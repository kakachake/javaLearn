package learn1;

public class Type {
    public static void main(String[] args) {
        int a = (int) '你';
        char b = (char) 20320;
        System.out.println(a);
        System.out.println(b);




        //进制转换
        int x = (int)34.89;
        System.out.println(x);

        byte y = (byte) 100000;
        System.out.println(y);
    }
}
